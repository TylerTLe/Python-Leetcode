# loops

# For Loop
for i in range(5):
    print(i)
    
# Iterating over a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
    
# While loop
count = 0

while count < 5:
    print(count)
    count += 1 
    
# Control Statements

# For loop break
for i in range(10):
    if i == 5:
        break
    print(i)
    
# For loop continue
for i in range (10):
    if i % 2 == 0:
        continue
    print(i)
    
